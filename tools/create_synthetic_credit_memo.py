    output_path.parent.mkdir(parents=True, exist_ok=True)
    doc = fitz.open()
    lines: list[tuple[str, str]] = [
        ("heading", "CRAIG SYNTHETIC MIDDLE MARKET CREDIT MEMO"),
        ("body", "This PDF is a fully synthetic testing fixture for Tag Studio. It is designed to look like a middle market credit memo while containing no real customer, borrower, or bank information."),
        ("body", "Memo Type: Renewal. Facility Type: Revolver. Borrower Segment: Middle Market. Review Boundary: as-of memo review only."),
    ]
    for heading, paragraphs in SECTIONS:
        lines.append(("heading", heading))
        for paragraph in paragraphs:
            lines.append(("body", paragraph))
    write_lines(doc, lines)
    doc.set_metadata(
        {
            "title": "Synthetic Middle Market Credit Memo",
            "author": "Tag Studio",
            "subject": "Synthetic credit memo fixture for local tagging tests",
            "keywords": "synthetic, credit memo, tagging, CRAIG, Tag Studio",
        }
    )
    doc.save(output_path)
    doc.close()



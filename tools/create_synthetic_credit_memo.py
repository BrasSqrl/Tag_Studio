def add_header_footer(page: fitz.Page, page_number: int) -> None:
    width = page.rect.width
    page.draw_rect(fitz.Rect(36, 28, width - 36, 58), color=(0.08, 0.19, 0.30), fill=(0.08, 0.19, 0.30))
    page.insert_text((48, 48), "SYNTHETIC CREDIT MEMO - TAG STUDIO TEST FIXTURE", fontsize=10, color=(1, 1, 1))
    page.insert_text((48, page.rect.height - 32), "Synthetic data only. No real borrower or customer information.", fontsize=8, color=(0.35, 0.35, 0.35))
    page.insert_text((width - 82, page.rect.height - 32), f"Page {page_number}", fontsize=8, color=(0.35, 0.35, 0.35))


def write_lines(doc: fitz.Document, lines: list[tuple[str, str]]) -> None:
    page_number = 1
    page = doc.new_page(width=612, height=792)
    add_header_footer(page, page_number)
    y = 82
    margin_left = 48
    max_y = 728

    for kind, text in lines:
        if kind == "heading":
            wrapped = [text]
            font_size = 12
            line_height = 18
            color = (0.05, 0.20, 0.32)
        else:
            wrapped = textwrap.wrap(text, width=92)
            font_size = 9.5
            line_height = 13
            color = (0.08, 0.08, 0.08)

        needed = len(wrapped) * line_height + (10 if kind == "heading" else 8)
        if y + needed > max_y:
            page_number += 1
            page = doc.new_page(width=612, height=792)
            add_header_footer(page, page_number)
            y = 82

        if kind == "heading":
            page.draw_rect(fitz.Rect(margin_left, y - 12, 564, y + 8), color=(0.88, 0.93, 0.96), fill=(0.88, 0.93, 0.96))
            y += 2

        for line in wrapped:
            page.insert_text((margin_left, y), line, fontsize=font_size, color=color)
            y += line_height
        y += 8


